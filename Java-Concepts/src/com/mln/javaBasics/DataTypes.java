package com.mln.javaBasics;

public class DataTypes {
	

	public static void main(String[] args) {


		//int is a primitive data type in java which store integer values
		int iVar1 = -1;
		int iVar2 =0;
		int iVar3 = 1;
		System.out.println(iVar1+iVar2+iVar3);
		
		//double is a primitive data type in java which store decimal values 
		double dVar1 = -1.2;
		double dVar2 = 0.0;
		double dVar3 = 1.5678;
		double dVar4 = 100;
		System.out.println(dVar1+dVar2+dVar3+dVar4);
		
		
		//char is a primitive data type in java which store character values 
		char cVar1 ='c';
		char cVar2 ='D';
		char cVar3 ='0';
		char cVar4 = '5' ;
			
		System.out.println(cVar1+cVar2+cVar3+cVar4);
		
		//String is a class to store String values 
		String sVar1 = "c";
		String sVar2 = "-1.5";
		String sVar3 = "This is my 1st Java Program";
		
		System.out.println(sVar1+sVar2+sVar3);
	}
	
	

}
